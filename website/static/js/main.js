function open_menu() {
    let menu = document.querySelector('#menu');
    menu.style.display = 'flex';
}

function close_menu() {
    let menu = document.querySelector('#menu');
    menu.style.display = 'none';
}

function open_dash_menu() {
    let menu = document.querySelector('#dash');
    menu.style.display = 'flex';
}

function close_dash_menu() {
    let menu = document.querySelector('#dash');
    menu.style.display = 'none';
}